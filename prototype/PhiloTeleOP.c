#include "JoystickDriver.c"

int leftJoy=0;
int rightJoy=0;

void moveAround(int rawLeftJoy, int rawRightJoy){



		//movement

		if(abs(rawLeftJoy/128.0*100) > 5)
			leftJoy=rawLeftJoy/128.0*100;
		else
			leftJoy = 0;
		if(abs(rawRightJoy/128.0*100) > 5)
			rightJoy=rawRightJoy/128.0*100;
		else
			rightJoy = 0;

		motor[FrontR] =-rightJoy;
		motor[FrontL] = leftJoy;
		motor[BackR] = -rightJoy;
		motor[BackL] = leftJoy;

}
void controlFans(bool fan){
	if(!fan){
		return;
	}
	static bool wasOnLastTime = false;
	if(fan && !wasOnLastTime){
		motor[FanL] = 60;
		motor[FanR] = -60;
		wasOnLastTime = true;
	}
	else{
		motor[FanL] = 0;
		motor[FanR] = 0;
		wasOnLastTime = false;
	}
}
void moveBelt(){
	servo[transfer] = 0;
}
void moveIntake(bool intakeChoice)
{ if (!intakeChoice)
		return;
	static bool wasOnLastTime = false;
	if (wasOnLastTime)
	{
	servo[intake] = 128;
	wasOnLastTime = true;
	}
	else
	{
		servo [intake] = 255;
		wasOnLastTime = false;
	}

}
